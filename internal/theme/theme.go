// Package theme defines named color palettes for cosmobar.
package theme

import "sort"

type RGB struct{ R, G, B uint8 }

// Palette holds the colors a theme applies to segment text, pills, and gauges.
type Palette struct {
	Accent    RGB // primary — identity segments
	Secondary RGB // version-control segments
	Tertiary  RGB // metric segments
	Muted     RGB // ambient segments / separators
	OK        RGB // gauge low
	Warn      RGB // gauge mid
	Crit      RGB // gauge high
	Dark      RGB // contrast ink for light backgrounds
	Light     RGB // contrast ink for dark backgrounds
}

