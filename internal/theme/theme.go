// Get returns the named palette and whether it exists.
func Get(name string) (Palette, bool) {
	p, ok := palettes[name]
	return p, ok
}

// Names returns the sorted list of built-in theme names.
func Names() []string {
	out := make([]string, 0, len(palettes))
	for k := range palettes {
		out = append(out, k)
	}
	sort.Strings(out)
	return out
}
